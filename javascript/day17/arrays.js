// Arrays

let fruits = ["Apple","Mango","Orange"];

console.log(fruits);

// Access single element

console.log(fruits[0]);

// Array length

console.log(fruits.length);

// push()

fruits.push("Banana");

console.log(fruits);

// unshift()

fruits.unshift("Grapes");

console.log(fruits);

// Modify element

fruits[1] = "Pineapple";

console.log(fruits);

// pop()

fruits.pop();

console.log(fruits);

// shift()

fruits.shift();

console.log(fruits);