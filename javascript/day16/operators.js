// Arithmetic Operators

let a = 10;
let b = 5;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);

// Comparison Operators

console.log(a > b);
console.log(a == b);
console.log(a === b);

// Logical Operators

console.log(true && true);
console.log(true || false);
console.log(!true);